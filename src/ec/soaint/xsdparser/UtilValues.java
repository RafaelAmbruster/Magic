public class UtilValues {
    String oldvalue;
    String newvalue;

    public UtilValues(String oldvalue, String newvalue) {
        this.oldvalue = oldvalue;
        this.newvalue = newvalue;
    }

    public String getOldvalue() {
        return oldvalue;
    }

    public String getNewvalue() {
        return newvalue;
    }
}

